function TrollButton({ data, onActivate, onDisappear }) {
    const [clicks, setClicks] = useState(0);
    const [label, setLabel] = useState(data.name);
    const [position, setPosition] = useState({
        top: Math.random() * (window.innerHeight - 50),
        left: Math.random() * (window.innerWidth - 150)
    });

    const moveRandomly = () => {
        setPosition({
            top: Math.random() * (window.innerHeight - 50),
            left: Math.random() * (window.innerWidth - 150)
        });
    };

    const handleClick = () => {
        const newClicks = clicks + 1;
        setClicks(newClicks);

        if (data.content && data.content[newClicks - 1]) {
            setLabel("(ó﹏ò｡) " + data.content[newClicks - 1]);
        } else if (newClicks > (data.content?.length || 2)) {
            moveRandomly();
            setLabel("WAIT ( °ㅁ°) !!");
        }

        onActivate?.(data.name, newClicks);

        if (newClicks >= 5) {
            setLabel("(ᵕ—ᴗ—) *bye~*");
            setTimeout(() => onDisappear?.(), 1000);
        }
    };

    return (
        <button
            onClick={handleClick}
            className="clear-button"
            style={{
                position: "absolute",
                top: `${position.top}px`,
                left: `${position.left}px`,
                zIndex: 9999,
                transition: "opacity 0.6s ease"
            }}
        >
            {label}
        </button>
    );
}

