export function DotArtGallery(modalOpen) {
    const [index, setIndex] = useState(0);

    useEffect(() => {
        if (modalOpen) {
            setIndex((prev) => {
                let next;
                do {
                    next = Math.floor(Math.random() * art.length);
                } while (next === prev && art.length > 1);
                return next;
            });
        }
    }, [modalOpen]);

    return (
        <div style={{ textAlign: "center" }}>
            <DotArt art={art[index]} speed={10} />
        </div>
    );
}