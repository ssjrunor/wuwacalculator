export function TrollButtonsLayer({ setModalOpen }) {
    const [activeTroll, setActiveTroll] = useState(null);

    useEffect(() => {
        const lastIndex = parseInt(getPersistentValue("lastTrollIndex") || "-1");
        const shouldAppear = Math.random() < 0.00001;

        if (shouldAppear) {
            let nextIndex;
            do {
                nextIndex = Math.floor(Math.random() * trollData.length);
            } while (nextIndex === lastIndex && trollData.length > 1);

            setTimeout(() => {
                const troll = trollData[nextIndex];
                troll.randomSeed = Math.random();
                setActiveTroll(troll);
            }, 4000);
            setPersistentValue("lastTrollIndex", String(nextIndex));
        }
    }, []);

    const handleActivate = (name, clicks) => {
        if (clicks === 5) {
            setTimeout(() => {
                setModalOpen(true);
            }, 1000);
        }
    };

    const handleDisappear = () => {
        setActiveTroll(null);
    };

    if (!activeTroll) return null;

    return (
        <TrollButton
            data={activeTroll}
            onActivate={handleActivate}
            onDisappear={handleDisappear}
        />
    );
}

export default function DotArt({ art, speed = 30 }) {
    const [visible, setVisible] = useState("");

    useEffect(() => {
        let i = 0;
        const step = 10;
        const timer = setInterval(() => {
            i += step;
            if (i >= art.length) {
                setVisible(art);
                clearInterval(timer);
            } else {
                setVisible(art.slice(0, i));
            }
        }, speed);
        return () => clearInterval(timer);
    }, [art, speed]);

    return (
        <pre
            style={{
                margin: 'unset',
                fontFamily: "monospace",
                whiteSpace: "pre",
                lineHeight: 1,
                userSelect: "none"
            }}
        >
      {visible}
    </pre>
    );
}

